def aumentar(num=0, taxa=0):
    percento = int(taxa) / 100
    resultado = num + (num * percento)
    return resultado



def diminuir(num=0, taxa=0):
    percento = float(taxa) / 100
    resultado = num - (num * percento)
    return resultado



